package core

import (
	"testing"
)

func TestClassify(t *testing.T) {
	tests := []struct {
		code, typ, desc string
		expected         string
	}{
		// 1. Test
		{"E600", "Тест", "Періодичний тест", "test"},
		{"E601", "Тест", "Ручний тест", "test"},
		{"E602", "TEST", "Some description", "test"},

		// 2. Alarm
		{"E110", "Пожежа", "Пожежна тривога", "alarm"},
		{"E115", "Пожежа", "Пожежа з клавіатури", "alarm"},
		{"E101", "Тривога", "Персональна небезпека", "alarm"},
		{"E116", "Тривога", "Тривога трубопроводу", "alarm"},
		{"E130", "Тривога", "Вторгнення", "alarm"},
		{"E144", "Тривога тампера", "Тривога тампера", "alarm"},
		{"E423", "Напад", "Доступ під примусом", "alarm"},
		{"E131", "Тривога", "Вторгнення, зона периметр", "alarm"},

		// 3. Fault
		{"E300", "Система", "Несправність системи", "fault"}, // Falls back to full text maybe? 
		// Actually "Система" + "Несправність системи" -> fullText contains "несправ" -> fault
		{"E301", "Втрата основного живлення", "Відсутність основного електроживлення", "fault"},
		{"E302", "Проблема АКБ", "Резервне електроживлення нижче норми", "fault"},
		{"E143", "Несправність", "Несправність модуля розширення", "fault"},
		{"E350", "Втрата зв'язку з ПЦС", "Немає зв'язку", "fault"},

		// 4. Guard
		{"R400", "Постановка", "Постановка під охорону", "guard"},
		{"R459", "Постановка", "Останнє ввімкнення охорони", "guard"},

		// 5. Disguard / Restore
		{"E400", "Зняття", "Зняття з охорони", "disguard"},
		{"R301", "Норма основного живлення", "Основне електроживлення у нормі", "disguard"},
		{"R110", "Норма", "Відновлення пожежної тривоги", "disguard"},
		{"R144", "Норма тампера", "Норма тампера", "disguard"},
		{"E406", "Скасування тривоги", "Скасування користувачем", "disguard"},
		{"E200", "Скидання", "Скидання пожежі", "disguard"},

		// 6. Other / System
		{"E610", "Звіт", "Звіт: зв'язок із приладом є", "other"},
		{"E114", "Система", "Нагрів", "other"},
		{"E117", "Система", "Полум'я", "other"},
		{"E112", "Система", "Займання", "other"},
	}

	for _, tt := range tests {
		got := Classify(tt.code, tt.typ, tt.desc)
		if got != tt.expected {
			t.Errorf("Classify(%q, %q, %q) = %q; want %q", tt.code, tt.typ, tt.desc, got, tt.expected)
		}
	}
}
